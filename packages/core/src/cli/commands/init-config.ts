// ─── Directory Structure ──────────────────────────────────────────────────

function createDirectoryStructure(root: string): void {
  const dirs = [
    'workspace',
    'workspace/memory',
    'workspace/memory/wiki',
    'workspace/memory/wiki/entities',
    'workspace/memory/wiki/concepts',
    'workspace/memory/wiki/decisions',
    'workspace/memory/wiki/projects',
    'workspace/memory/wiki/areas',
    'workspace/memory/wiki/research',
    'workspace/memory/agents',
    'workspace/memory/00-inbox',
    'workspace/data',
    'workspace/data/lancedb',
    'workspace/data/logs',
    'data',
    'wiki',
    'memory',
  ];

  for (const dir of dirs) {
    const fullPath = join(root, dir);
    if (!existsSync(fullPath)) {
      mkdirSync(fullPath, { recursive: true });
    }
  }

  console.log('✅ Directory structure created:');
  console.log('   workspace/  (identity, wiki, memory, data)');
  console.log('   data/       (lancedb, logs)');
  console.log('   wiki/       (knowledge base)');
  console.log('   memory/     (memory store)');
}

