/**
 * Lodestone — Config Init Command
 *
 * `lodestone config init` — Generates a fully documented lodestone.config.yaml,
 * creates directory structure, and writes starter identity files.
 *
 * Options:
 *   --provider <name>    LLM provider (ollama|openai|anthropic)  default: ollama
 *   --model <name>       Model name                            default: glm-5.2:cloud
 *   --port <number>      WebChat port                           default: 3000
 *   --output <path>      Config output path                     default: ./lodestone.config.yaml
 *   --force              Overwrite existing config without asking
 *
 * Also creates:
 *   - workspace/         (identity files, wiki, data dirs)
 *   - data/              (lancedb, logs)
 *   - wiki/              (wiki structure)
 *   - memory/            (memory structure)
 *   - IDENTITY.md        (template)
 *   - SOUL.md            (template)
 */

import { existsSync, mkdirSync, writeFileSync, readFileSync } from 'fs';
import { resolve, dirname, join } from 'path';
import { createInterface } from 'readline';
import { getLogger } from '../../utils/logger.js';

// ─── Types ──────────────────────────────────────────────────────────────────

export interface ConfigInitOptions {
  provider?: string;
  model?: string;
  port?: number;
  output?: string;
  force?: boolean;
}

// ─── Defaults ──────────────────────────────────────────────────────────────

const DEFAULT_PROVIDER = 'ollama';
const DEFAULT_MODEL = 'glm-5.2:cloud';
const DEFAULT_PORT = 3000;

// ─── Command ────────────────────────────────────────────────────────────────

export async function configInitCommand(opts: ConfigInitOptions): Promise<void> {
  const log = getLogger('config-init');

  const provider = opts.provider || DEFAULT_PROVIDER;
  const model = opts.model || DEFAULT_MODEL;
  const port = opts.port || DEFAULT_PORT;
  const outputPath = resolve(opts.output || './lodestone.config.yaml');

  // Check if config already exists
  if (existsSync(outputPath) && !opts.force) {
    const shouldOverwrite = await askOverwrite(outputPath);
    if (!shouldOverwrite) {
      console.log('Cancelled. Existing config preserved.');
      return;
    }
  }

  // Generate config content
  const configContent = generateFullConfig({ provider, model, port });

  // Write config file
  const configDir = dirname(outputPath);
  if (!existsSync(configDir)) {
    mkdirSync(configDir, { recursive: true });
  }
  writeFileSync(outputPath, configContent);
  console.log(`✅ Config written to ${outputPath}`);

  // Create directory structure
  const root = dirname(outputPath);
  createDirectoryStructure(root);

  // Create starter identity files
  createStarterIdentity(root);

  console.log(`\n📋 Next steps:`);
  console.log(`   1. Edit ${outputPath} to customize your agent`);
  console.log(`   2. Add API keys to .env (if using OpenAI/Anthropic)`);
  console.log(`   3. Run \`lodestone start\` to boot your agent`);
}

// ─── Config Generator ──────────────────────────────────────────────────────

