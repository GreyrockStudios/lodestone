// ─── Overwrite Prompt ────────────────────────────────────────────────────────

async function askOverwrite(path: string): Promise<boolean> {
  // Non-TTY: don't overwrite by default
  if (!process.stdin.isTTY) {
    console.log(`⚠️  Config already exists at ${path}. Use --force to overwrite.`);
    return false;
  }

  const rl = createInterface({ input: process.stdin, output: process.stdout });
  return new Promise<boolean>((resolve) => {
    rl.question(`Config already exists at ${path}. Overwrite? [y/N]: `, (answer) => {
      rl.close();
      return answer.trim().toLowerCase() === 'y' || answer.trim().toLowerCase() === 'yes';
    });
  });
}

// ─── CLI Argument Parser ────────────────────────────────────────────────────

export function parseConfigInitArgs(args: string[]): ConfigInitOptions {
  const opts: ConfigInitOptions = {};

  for (let i = 0; i < args.length; i++) {
    const arg = args[i];
    switch (arg) {
      case '--provider':
        opts.provider = args[++i];
        break;
      case '--model':
        opts.model = args[++i];
        break;
      case '--port':
        opts.port = parseInt(args[++i], 10);
        break;
      case '--output':
        opts.output = args[++i];
        break;
      case '--force':
        opts.force = true;
        break;
    }
  }

  return opts;
}